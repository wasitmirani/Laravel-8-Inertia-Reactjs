    {
        $users=User::latest()->get();
        return Inertia::render('User/UsersComponent',[
            'users'=>$users->map(function($user){
                return[
                    'id' => $user->id,
                    'name' => $user->name,
                    'email' => $user->email,
                    'edit_url' => URL::route('users.edit', $user),
                ];
            }),
            'create_url' => URL::route('users.create'),
        ])->withViewData(['title' => 'User CRUD']);;
    }

